
numero1 = int (input("Insira o primeiro numero da divisão :"))
numero2 = int (input("Insira o segundo numero da divisão :"))

#divisão com resultado float 
divisão_float = numero1 / numero2 
print(divisão_float)

#divisão com resultado inteiro
divisão_int = numero1 // numero2 
print(divisão_int)

#divisão com resto
divisão_resto = numero1 % numero2 
print(divisão_resto)


